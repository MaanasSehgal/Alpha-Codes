package Lec1;

public class datatypes {

  public static void main(String[] args) {
    byte b = 8;
    System.out.println(b);
    char ch = 'a';
    System.out.println(ch);
    boolean var = false;
    System.out.println(var);
    float price = 10.5f;
    System.out.println(price);
    int number = 25;
    System.out.println(number);
    //long
    //double
    short n = 240;
    System.out.println(n);
  }
}
